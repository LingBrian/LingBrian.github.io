function view(){
    var showcase_bg = document.getElementById("showcase_bg");
    showcase_bg.style.display = "block";
    var showcase = document.getElementById("showcase");
    showcase.style.display = "block";
}
function view_close(){
    var showcase_bg = document.getElementById("showcase_bg");
    showcase_bg.style.display = "none";
    var showcase = document.getElementById("showcase");
    showcase.style.display = "none";
}